package Kyu.Skifty.Util;

public class S {

    /*
    Class is intended for the Language System
    Stores the placeholder and the value it is replaced by
    Short name for less line length
     */

    private String s1, s2;

    public S(String s1, String s2) {
        this.s1 = s1;
        this.s2 = s2;
    }

    public String getS1() {
        return s1;
    }

    public String getS2() {
        return s2;
    }
}
